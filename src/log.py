def info(msg):
    """
    print info message [*]
    """
    print(f"[*] {msg}")

def success(msg):
    """
    print success message [+] 
    """
    print(f"[+] {msg}")

def error(msg):
    """
    print error message [!]
    """
    print(f"[!] {msg}")

def debug(msg):
    """
    print debug message [?]
    """
    print(f"[?] {msg}")
